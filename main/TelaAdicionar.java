    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Nome.getText().equals("") || (Telefone.getText().equals("") && Email.getText().equals(""))){
            JOptionPane.showMessageDialog(null,  "Preencha pelo menos o nome e o telefone ou e-mail!", "Contato inv√°lido!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Contato aux = new Contato(Nome.getText(), Sobrenome.getText(), Telefone.getText(), Email.getText(), Endereco.getText(), numero.getText(),  CEP.getText());
            aux.setNome(Nome.getText());
            aux.setSobrenome(Sobrenome.getText());
            nova.insere(aux);
            nova.ordenaLista();
            FileWriter fw;
                StringBuilder sb = new StringBuilder("");
                    for(int i = 0; i < nova.getNumeroDeContatos(); i++){
                        sb.append(nova.getListaDeContatos().get(i).getNome()+"\n"+ nova.getListaDeContatos().get(i).getSobrenome()+"\n"+nova.getListaDeContatos().get(i).getTelefone()+"\n"+nova.getListaDeContatos().get(i).getEmail()+"\n"+nova.getListaDeContatos().get(i).getRua()+"\n"+nova.getListaDeContatos().get(i).getNumero()+"\n"+nova.getListaDeContatos().get(i).getCep()+"\n");
                    }
            try {
                fw = new FileWriter(f.getName());
                fw.write(sb.toString());
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(TelaAdicionar.class.getName()).log(Level.SEVERE, null, ex);
            }
            ListaAgenda tela = new ListaAgenda(nova,f);
            tela.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

