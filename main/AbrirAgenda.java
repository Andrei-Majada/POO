    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nome.getText().equals("")){
            JOptionPane.showMessageDialog(null,  "Preencha o nome!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            File f = new File (nome.getText()+".txt");
            FileReader arq;
            try {
                arq = new FileReader(f.getName());
                BufferedReader lerarq = new BufferedReader(arq);
                String nome2, sobrenome, telefone, email, rua, numero, cep;
                try {
                    while((nome2 = lerarq.readLine()) != null){
                        sobrenome = lerarq.readLine();
                        telefone = lerarq.readLine();
                        email = lerarq.readLine();
                        rua = lerarq.readLine();
                        numero = lerarq.readLine();
                        cep = lerarq.readLine();
                        Contato aux = new Contato (nome2, sobrenome, telefone, email, rua, numero, cep);
                        nova.insere(aux);
                    }
                    lerarq.close();
                } catch (IOException ex) {
                    Logger.getLogger(AbrirAgenda.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(AbrirAgenda.class.getName()).log(Level.SEVERE, null, ex);
            }
            ListaAgenda tela = new ListaAgenda(nova, f);
            tela.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

